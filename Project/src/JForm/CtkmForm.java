    private void btnThemCTCTKMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemCTCTKMActionPerformed
        String []str = {
            lblMaCTKM.getText(),
            monAnBLL.getMaMonByTenMon(String.valueOf(cbxMonOfKm.getSelectedItem())),
            txtTienGiam.getText()
        };
        for(int i = 0 ; i < tblCTCTKM.getRowCount() ; i++){
            if(tblCTCTKM.getValueAt(i,0).equals(monAnBLL.getMaMonByTenMon((String)cbxMonOfKm.getSelectedItem()))){
                JOptionPane.showMessageDialog(this, "sản phẩm đã có trong chương trình khuyến mãi","Lỗi",JOptionPane.ERROR_MESSAGE);
                return;
            }   
        }
        cTChuongTrinhKMBLL.ThemCTCTKM(str);
        JOptionPane.showMessageDialog(this,"Thêm thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        loadTableCTCTKM();
        
        if(trang_thai == 1){    
            String ma_mon = monAnBLL.getMaMonByTenMon(String.valueOf(cbxMonOfKm.getSelectedItem()));
            int gia_giam = Integer.parseInt(txtTienGiam.getText());

            new MonAnBLL().suaGiaMonTheoKM(ma_mon,gia_giam);
            new AdminForm().load();
        }
        setNullCTCTKM();
    }//GEN-LAST:event_btnThemCTCTKMActionPerformed

