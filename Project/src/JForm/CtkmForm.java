    private void btnDelCTCTKMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelCTCTKMActionPerformed
        int kq = JOptionPane.showConfirmDialog(this,"bạn có thật sự muốn xóa","Thông báo",JOptionPane.YES_OPTION);
        if(kq==0){
            int ma_ctkm = Integer.parseInt(lblMaCTKM.getText());
            String ma_mon = monAnBLL.getMaMonByTenMon(String.valueOf(cbxMonOfKm.getSelectedItem()));

            if(trang_thai == 1){

                int gia_giam = 0;

                new MonAnBLL().suaGiaMonTheoKM(ma_mon,gia_giam);
            }
            cTChuongTrinhKMBLL.delCTCTKM(ma_ctkm, ma_mon);
            loadTableCTCTKM();
            setNullCTCTKM();
        }
    }//GEN-LAST:event_btnDelCTCTKMActionPerformed

    private void tblCTCTKMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCTCTKMMouseClicked
        int click = tblCTCTKM.getSelectedRow();
        if(click >= 0){
            String ma_mon = String.valueOf(tblCTCTKM.getValueAt(click,0));
            
            CTChuongTrinhKMDTO cTChuongTrinhKMDTO = cTChuongTrinhKMBLL.getCtctkmByMaMon(ma_mon, ma_ctkm);
            System.out.println(ma_mon);
            System.out.println(ma_ctkm);
            if(cTChuongTrinhKMDTO != null){
                cbLoaiOfKm.setSelectedItem(cTChuongTrinhKMDTO.getTen_loai());
                cbxMonOfKm.setSelectedItem(cTChuongTrinhKMDTO.getTen_mon());
                txtTienGiam.setText(cTChuongTrinhKMDTO.getTien_giam()+"");
            }
        }
    }//GEN-LAST:event_tblCTCTKMMouseClicked

